package pl.yummy.infrastructure.database.entity.enums;

public enum DietType {

    VEGETARIAN,
    VEGAN,
    GLUTEN_FREE,
    LACTOSE_FREE,
    KETO,
    PALEO,
    HALAL,
    KOSHER,
    LOW_CARB,
    HIGH_PROTEIN,
    PESCATARIAN,
    RAW_FOOD,
    MEDITERRANEAN,
    DIABETIC,
    LOW_SODIUM


    /*
    VEGETARIAN – Dieta wegetariańska (bez mięsa, ale z produktami pochodzenia zwierzęcego, np. jajkami, mlekiem).
    VEGAN – Dieta wegańska (bez produktów odzwierzęcych, np. mleka, jajek, miodu).
    GLUTEN_FREE – Dieta bezglutenowa (dla osób z nietolerancją glutenu lub celiakią).
    LACTOSE_FREE – Dieta bezlaktozowa (dla osób nietolerujących laktozy).
    KETO – Dieta ketogeniczna (niskowęglowodanowa, wysokotłuszczowa).
    PALEO – Dieta paleo (inspirowana dietą pierwotną, oparta na nieprzetworzonych produktach).
    HALAL – Dieta halal (zgodna z zasadami religii islamskiej).
    KOSHER – Dieta koszerna (zgodna z zasadami religii żydowskiej).
    LOW_CARB – Dieta niskowęglowodanowa.
    HIGH_PROTEIN – Dieta wysokobiałkowa.
    PESCATARIAN – Dieta pescetariańska (bez mięsa, ale z rybami i owocami morza).
    RAW_FOOD – Dieta surowa (oparta na surowych produktach roślinnych).
    MEDITERRANEAN – Dieta śródziemnomorska (bazująca na warzywach, owocach, rybach, oliwie z oliwek).
    DIABETIC – Dieta dla diabetyków (kontrola poziomu cukru).
    LOW_SODIUM – Dieta niskosodowa (o ograniczonej zawartości soli).

     */
}
